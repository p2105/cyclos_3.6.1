package nl.strohalm.cyclos.webservices.interceptor;

import nl.strohalm.cyclos.webservices.WebServiceContext;

import org.apache.cxf.binding.soap.SoapMessage;
import org.apache.cxf.binding.soap.interceptor.AbstractSoapInterceptor;
import org.apache.cxf.interceptor.Fault;
import org.apache.cxf.phase.Phase;

/**
 * An interceptor which cleans up the context
 * 
 * @author luis
 */
public class CleanupInterceptor extends AbstractSoapInterceptor {

    public CleanupInterceptor() {
        super(Phase.MARSHAL);
    }

    public void handleMessage(final SoapMessage message) throws Fault {
        WebServiceContext.cleanup();
    }
}
